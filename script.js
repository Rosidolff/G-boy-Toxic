let escenaActual = 0;
let escribiendo = false; // Indica si se está escribiendo el texto

function actualizarEscena() {
    let escena = escenas[escenaActual];
    let fondo = document.getElementById('fondo');
    let personaje = document.getElementById('personaje');
    let nombrePersonaje = document.getElementById('nombrePersonaje');
    let textoDialogo = document.getElementById('textoDialogo');
    let musicaFondo = document.getElementById('musicaFondo');

    // Cambios solo si el fondo o el personaje son diferentes
    if (fondo.src !== escena.fondo) {
        fondo.src = escena.fondo;
        fondo.style.opacity = 1;
    }
    if (personaje.src !== escena.personaje) {
        personaje.src = escena.personaje;
        personaje.style.opacity = 1;
    }

    nombrePersonaje.innerText = escena.nombre;
    textoDialogo.innerHTML = '';
    efectoMaquinaDeEscribir(escena.dialogo);

    // Cambiar música de fondo si es necesario
    if (musicaFondo.getAttribute('src') !== escena.musicaFondo) {
        musicaFondo.src = escena.musicaFondo;
        musicaFondo.play();
    }
}

function efectoMaquinaDeEscribir(texto) {
    let textoDialogo = document.getElementById('textoDialogo');
    let i = 0;
    escribiendo = true;

    function escribirLetra() {
        if (i < texto.length) {
            textoDialogo.innerHTML += texto.charAt(i);
            i++;
            if (escribiendo) {
                setTimeout(escribirLetra, 20); // Ajusta la velocidad de escritura aquí
            }
        } else {
            escribiendo = false;
        }
    }
    escribirLetra();
}

document.addEventListener('keydown', function (event) {
    if (event.code == 'Space') {
        if (escribiendo) {
            // Completar inmediatamente el texto y detener la escritura
            let escena = escenas[escenaActual];
            document.getElementById('textoDialogo').innerHTML = escena.dialogo;
            escribiendo = false;
            return;
        }
        escenaActual++;
        if (escenaActual < escenas.length) {
            actualizarEscena();
        } else {
            // Fin del juego o reiniciar
            escenaActual = 0;
            actualizarEscena();
        }
    } else if (event.code == 'ArrowLeft') {
        if (escenaActual > 0) {
            escenaActual--;
            actualizarEscena();
        }
    }
});


// Iniciar con la primera escena
actualizarEscena();
